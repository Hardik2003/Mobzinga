package com.pojo;

public class UserMasterLogin {

	private int userID;
	private double userPhone;
	private String userLocation;
	private int userAdRange;

	public int getUserID() {
		return userID;
	}

	public void setUserID(int userID) {
		this.userID = userID;
	}

	public double getUserPhone() {
		return userPhone;
	}

	public void setUserPhone(double userPhone) {
		this.userPhone = userPhone;
	}

	public String getUserLocation() {
		return userLocation;
	}

	public void setUserLocation(String userLocation) {
		this.userLocation = userLocation;
	}

	public int getUserAdRange() {
		return userAdRange;
	}

	public void setUserAdRange(int userAdRange) {
		this.userAdRange = userAdRange;
	}

}
